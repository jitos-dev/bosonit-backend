package com.bosonit.garciajuanjo;

public class Calculadora {

    public Integer suma(int valUno, int valDos) {
        return valUno + valDos;
    }

    public Integer resta(int valUno, int valDos) {
        return valUno - valDos;
    }

    public Integer multiplicacion(int valUno, int valDos) {
        return valUno * valDos;
    }

    public Double division(int valUno, int valDos) {
        return (double) (valUno / valDos);
    }
}
